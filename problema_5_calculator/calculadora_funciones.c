{
 printf("x - y =%.1lf + %.1lf = %.1lf\n",x, y, x+y);
 
}
  
double resta(double x, double y)
{
 printf("x + y = %.1lf - %.1lf = %.1lf\n",x, y, x-y);
  
}
  
double multiplicacion(double x, double y)
{
 printf("x * y = %.1lf * %.1lf = %.1lf\n",x, y, x*y);
 
}  
  
double division(double x, double y)
{
 printf("x / y = %.1lf / %.1lf = %.1lf\n",x, y, x/y);
 
}  
   
